/**
 * @brief Imprime a arte e o texto de um slide, com contador de progresso.
 *
 * A arte e impressa linha a linha sem centralizacao adicional, pois ja
 * vem pronta na largura correta (mesmo padrao de imprimirTitulo). Apenas
 * o texto narrativo e o contador sao centralizados.
 *
 * @param s      Slide a ser exibido.
 * @param indice Indice (0-based) do slide atual.
 * @param total  Numero total de slides.
 */
static void exibirSlide(const Slide &s, int indice, int total)
{
    system("cls");
    for (const auto &linha : s.arte)
        cout << linha << "\n";
    cout << "\n";
    imprimirCentralizado(s.texto);
    cout << "\n";
    imprimirCentralizado("(" + to_string(indice + 1) + "/" + to_string(total) + ")");
}

