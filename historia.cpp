/**
 * @brief Le uma tecla de navegacao, descartando bytes de teclas especiais.
 *
 * Teclas especiais (setas, F1-F12 etc.) chegam em dois bytes: o primeiro
 * e 0 ou 224, seguido de um segundo byte que identifica a tecla. Como a
 * historia nao usa essas teclas, o segundo byte e descartado e a funcao
 * retorna -1 para sinalizar "tecla irrelevante".
 *
 * @return Codigo ASCII da tecla (13 Enter, 8 Backspace, 32 Espaco) ou -1.
 */
static int lerTeclaNavegacao()
{
    int tecla = _getch();
    if (tecla == 0 || tecla == 224)
    {
        _getch(); // descarta segundo byte de tecla especial
        return -1;
    }
    return tecla;
}

bool exibirHistoria()
{
    vector<Slide> slides = montarSlides();
    int i = 0;
    int total = (int)slides.size();

    while (true)
    {
        bool ultimo = (i == total - 1);

        exibirSlide(slides[i], i, total);
        cout << "\n";
        imprimirSeparador("-");

        if (ultimo)
            imprimirCentralizado("Voce(s) aceitam?", VERMELHO_B);
        else
            imprimirCentralizado("[Backspace] voltar     [Enter] avancar     [Espaco] sair");

        int tecla = lerTeclaNavegacao();

        if (tecla == ' ')
            return false; // sai a qualquer momento

        if (tecla == 13) // Enter
        {
            if (ultimo) return true; // aceitou -> inicia o jogo
            i++;
            continue;
        }

        if (tecla == 8) // Backspace
        {
            if (ultimo) return false;  // no ultimo slide, Backspace sai (mesmo que Espaco)
            if (i == 0) return false;  // no primeiro slide, Backspace tambem sai
            i--;
            continue;
        }

        // qualquer outra tecla: ignora e redesenha o mesmo slide
    }
}
